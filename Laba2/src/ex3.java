/*
Рисуем треугольник 
Используя цикл for вывести 
на экран прямоугольный треугольник 
из восьмёрок со сторонами 10 и 10. 
 */
public class ex3 {
    public static void main(String[] args) {
        String s ="";
        for (int i = 0; i < 10; i++) {
            s += "8";
            System.out.println(s);
        }
    }
}
